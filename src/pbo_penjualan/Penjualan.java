    public void tampilkanData(){
        tabel_penjualan = new DefaultTableModel();
        tabel_penjualan.addColumn("ID Transaksi");
        tabel_penjualan.addColumn("Nama Customer");
        tabel_penjualan.addColumn("Nama Produk");
        tabel_penjualan.addColumn("Harga");
        tabel_penjualan.addColumn("Jumlah");
        tabel_penjualan.addColumn("Total");
        
        penjualanTBL.setModel(tabel_penjualan);
        
        Connection c = koneksi.getKoneksi();
        try{
            Statement s = c.createStatement();
            query = "SELECT * FROM transaksi";
            ResultSet r = s.executeQuery(query);
            
            while(r.next()){
                tabel_penjualan.addRow(new Object[]{
                    r.getString("ID_Transaksi"),
                    r.getString("Nama"),
                    r.getString("Nama_Produk"),
                    r.getString("Harga"),
                    r.getString("Jumlah"),
                    r.getString("Total"),
                });
            }
        } catch(SQLException e){
            System.out.println(e);
        }
    }
    
