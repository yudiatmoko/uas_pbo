    private void penjualanTBLMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_penjualanTBLMouseClicked
        // TODO add your handling code here:
        int baris = penjualanTBL.rowAtPoint(evt.getPoint());
        String idtrans = penjualanTBL.getValueAt(baris, 0).toString();
        IDTransaksiTF.setText(idtrans);
        String nama = penjualanTBL.getValueAt(baris, 1).toString();
        NamaTF.setText(nama);
        String namaProduk = penjualanTBL.getValueAt(baris, 2).toString();
        NamaProdukTF.setText(namaProduk);
        String harga = penjualanTBL.getValueAt(baris, 3).toString();
        HargaTF.setText(harga);
        String jumlah = penjualanTBL.getValueAt(baris, 4).toString();
        JumlahTF.setText(jumlah);
        String total = penjualanTBL.getValueAt(baris, 5).toString();
        TotalTF1.setText(total);
    }//GEN-LAST:event_penjualanTBLMouseClicked

